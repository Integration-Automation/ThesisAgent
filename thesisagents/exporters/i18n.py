def normalise_language(language: str | None) -> str:
    """Lowercase + dash-normalise a language tag. Falls back to DEFAULT_LANGUAGE."""
    if not language:
        return DEFAULT_LANGUAGE
    canonical = language.strip().lower().replace("_", "-")
    return canonical if canonical in _TABLE else DEFAULT_LANGUAGE


def strings_for(language: str | None) -> dict[str, str]:
    """Return the full string table for the given language (or English fallback)."""
    return _TABLE[normalise_language(language)]


def t(language: str | None, key: str, **format_kwargs: object) -> str:
    """Shortcut: translate one key with optional ``.format()`` substitution."""
    value = strings_for(language).get(key, _TABLE[DEFAULT_LANGUAGE].get(key, key))
    if format_kwargs:
        return value.format(**format_kwargs)
    return value
